            <p>Born and raised in France, Bouzereau moved to New York in the early 1980s and worked in several areas of the independent film industry.  During this time he also wrote his first book, THE DE PALMA CUT, a study of the films of director Brian De Palma.  Bouzereau moved to Hollywood, California in 1990 and worked in feature film development for Bette Midler at Disney Studios, while continuing his passion for writing books on cinema.</p>

            <p>In 1994, Bouzereau wrote, directed and produced his first of many documentaries for Steven Spielberg, hence joining the home entertainment revolution.  Bouzereau has directed, written and produced hundreds of documentaries on the “making of” the biggest films in the history of cinema, by some of the most acclaimed directors of all-time, including: Steven Spielberg, James Cameron, Alfred Hitchcock, David Lean, Martin Scorsese, Brian De Palma, William Friedkin, George Lucas, Robert Zemeckis, Warren Beatty, Roman Polanski, Peter Bogdanovich and others. </p>

            <p>As an author, Bouzereau’s credits include HITCHCOCK: PIECE BY PIECE, THE ART OF BOND, STAR WARS: THE ANNOTATED SCREENPLAYS, THE CUTTING ROOM FLOOR, ULTRA VIOLENT MOVIES and others. He most recently contributed to LINCOLN: A CINEMATIC AND HISTORICAL COMPANION.</p>

